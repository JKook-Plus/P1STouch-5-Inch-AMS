def upscale_bitmap(bitmap, width, height, scale_factor):
    img = Image.fromarray(bitmap).convert('L')
    img = img.resize((width * scale_factor, height * scale_factor), Image.NEAREST)
    return np.array(img)

def extract_bitmap(glyph_bitmap, glyph_dsc):
    bitmaps = []
    for i, dsc in enumerate(glyph_dsc):
        index = dsc["bitmap_index"]
        width = dsc["box_w"]
        height = dsc["box_h"]
        bitmap_size = width * height

        if width == 0 or height == 0:
            bitmaps.append(np.array([]))
            continue

        bitmap = glyph_bitmap[index:index + bitmap_size]
        
        # Debugging output
        print(f"Processing glyph {i}: index={index}, width={width}, height={height}, bitmap_size={bitmap_size}, actual_data_length={len(bitmap)}")

        # Ensure that the data length matches the expected size
        if len(bitmap) != bitmap_size:
            print(f"Error: Mismatch in bitmap size for glyph {i}. Expected {bitmap_size} but got {len(bitmap)}.")
            continue

        bitmap = np.array(bitmap).reshape((height, width))
        bitmaps.append(bitmap)
    return bitmaps

def regenerate_glyph_bitmap_and_dsc(bitmaps, glyph_dsc, scale_factor):
    new_glyph_bitmap = []
    new_glyph_dsc = []
    bitmap_index = 0

    for i, bitmap in enumerate(bitmaps):
        if bitmap.size == 0:
            new_glyph_dsc.append(glyph_dsc[i])
            continue
        
        width = glyph_dsc[i]["box_w"]
        height = glyph_dsc[i]["box_h"]

        upscaled_bitmap = upscale_bitmap(bitmap, width, height, scale_factor)
        new_bitmap_flat = upscaled_bitmap.flatten().tolist()

        new_glyph_dsc.append({
            "bitmap_index": bitmap_index,
            "adv_w": glyph_dsc[i]["adv_w"] * scale_factor,
            "box_w": width * scale_factor,
            "box_h": height * scale_factor,
            "ofs_x": glyph_dsc[i]["ofs_x"] * scale_factor,
            "ofs_y": glyph_dsc[i]["ofs_y"] * scale_factor
        })

        bitmap_index += len(new_bitmap_flat)
        new_glyph_bitmap.extend(new_bitmap_flat)

    return new_glyph_bitmap, new_glyph_dsc

def format_bitmap_as_hex(bitmap):
    formatted_bitmap = []
    for i, byte in enumerate(bitmap):
        if i % 8 == 0 and i != 0:
            formatted_bitmap.append("\n")
        formatted_bitmap.append(f"0x{byte:02x}")
    return "\n".join([" ".join(formatted_bitmap[i:i+8]) for i in range(0, len(formatted_bitmap), 8)])

def save_to_file(glyph_bitmap, glyph_dsc, output_file):
    formatted_bitmap = format_bitmap_as_hex(glyph_bitmap)
    data = {
        "glyph_bitmap": formatted_bitmap,
        "glyph_dsc": glyph_dsc
    }
    with open(output_file, 'w') as f:
        json.dump(data, f, indent=4)

